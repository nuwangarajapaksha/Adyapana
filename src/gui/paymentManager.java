    private void paymentTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_paymentTableMouseClicked
        // TODO add your handling code here:
        int row = paymentTable.getSelectedRow();
        int invoiceNo = (int) paymentTableModel.getValueAt(row, 0);
        Object[] paymentData = paymentModel.getSelectedpaymentData(invoiceNo);

        invoiceNoField.setText(new String(paymentData[0] + ""));
        studentNoField.setText(new String(paymentData[1] + ""));
        studentNameField.setText((String) paymentData[2]);
        teacherNameComboBox.setSelectedIndex((int) paymentData[3] - 1);
        subjectNameComboBox.setSelectedIndex((int) paymentData[4] - 1);
        monthComboBox.setSelectedItem((Object) paymentData[5]);
        valueField.setText(new String(paymentData[6] + ""));
    }//GEN-LAST:event_paymentTableMouseClicked

