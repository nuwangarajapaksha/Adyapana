    private void insertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertButtonActionPerformed
        // TODO add your handling code here:
        //String paymentNo = paymentNoField.getText();
        int studentNo = Integer.parseInt(studentNoField.getText());
        int teacher = teacherNameComboBox.getSelectedIndex() + 1;
        int subject = subjectNameComboBox.getSelectedIndex() + 1;
        String month = String.valueOf(monthComboBox.getSelectedItem());

        String value = valueField.getText();

        String result = paymentModel.insertpayment(studentNo, teacher, subject, month, value);
        JOptionPane.showConfirmDialog(rootPane, result, "Message", -1, 1);
        paymentModel.loadpaymentTable("", paymentTableModel);

        invoiceNoField.setText("");
        valueField.setText("");
    }//GEN-LAST:event_insertButtonActionPerformed

